package com.operation;

public class OperationsImpl implements Operations {
    public int sumNumber(int number, int number1) {
        return number+number1;
    }

    public int subNumber(int number, int number1) {
        return number-number1;
    }

    public int multiplicationNumber(int number, int number1) {
        return number*number1;
    }

    public int divisionNumber(int number, int number1) {
        return number/number1;
    }
}
